public class Swapping {
    public static void main(String[] args){
        int num1=5;
        int num2=6;
        int temp;
        System.out.println("X :"+num1);
        System.out.println("y :"+num2);
        temp=num1;
        num1=num2;
        num2=temp;
        System.out.println("X :"+num1);
        System.out.println("Y :"+num2);
    }
}
